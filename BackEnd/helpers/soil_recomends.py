def recommend_next_crop(soil_type, previous_crop, season):
    if soil_type not in soils:
        return "Soil data not available"

    if previous_crop not in soils[soil_type]:
        return "Crop data not available for this soil type"

    if season in soils[soil_type][previous_crop]["successor"]:
        successor = soils[soil_type][previous_crop]["successor"][season]
        return f"Recommended next crop for {soil_type} soil after {previous_crop} in {season} is {successor}"
    else:
        return f"Fallow period recommended for {soil_type} soil after {previous_crop} in {season}. Planting the same crop consecutively might not be ideal due to potential nutrient depletion or disease buildup."
